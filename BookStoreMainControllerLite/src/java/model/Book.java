package model;

/**
 *
 * @author Admin
 */
public class Book {
    private String id;
    private String name;
    private int page;
    private String author;
    private float price;
    private boolean status;

    public Book() {
    }

    public Book(String id, String name, int page, String author, float price, boolean status) {
        this.id = id;
        this.name = name;
        this.page = page;
        this.author = author;
        this.price = price;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
    
}
